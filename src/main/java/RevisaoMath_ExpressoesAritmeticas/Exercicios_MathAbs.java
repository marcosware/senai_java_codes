package RevisaoMath_ExpressoesAritmeticas;

public class Exercicios_MathAbs {
    public static void main(String[] args) {
        System.out.println("EXERCÍCIOS PARA MATH.ABS\n");
        System.out.println("1. Qual é o valor absoluto de -10?");
        System.out.printf("Resultado: %d\n\n", Math.abs(-10));
        System.out.println("2. Encontre o valor absoluto de 15.");
        System.out.printf("Resultado: %d\n\n", Math.abs(15));
        System.out.println("3. Determine o valor absoluto de 0.");
        System.out.printf("Resultado: %d\n\n", Math.abs(0));
    }
}
